    private void quantityAddButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_quantityAddButtonMouseClicked
        if(quantityTextField.getText() != null && !itemToCheck.equals("") && itemToCheck != null){
            String quantity = quantityTextField.getText();
            int quant = 0;
            try {
              quant = Integer.parseInt(quantity);
            } 
            catch (NumberFormatException e) {
                System.out.println("Invalid Input");
            }
            System.out.println(quant);
            if(quant > 0 ){
                if(cart.itemExists(itemToCheck)){
                     cart.getItem(itemToCheck).setQuantity(cart.getItem(itemToCheck).getQuantity() + quant);
                     setCartList();
                }
                else{
                    cart.addItem(new Item(itemToCheck, inventory.getItem(itemToCheck).getItemType(),
                                 quant, 0, inventory.getItem(itemToCheck).getValue()));
                    setCartList();
                }
            }
        }
    }//GEN-LAST:event_quantityAddButtonMouseClicked

    private void setCartList(){
         ((DefaultListModel)cartList.getModel()).clear();
         ArrayList<Item> items = cart.getItems();
         for(int i = 0;i < items.size();i++){
              ((DefaultListModel)cartList.getModel()).addElement("<html>" +items.get(i).getItemName() +
                "<br>Quantity: " + items.get(i).getQuantity() + "</html>");
            }
         }

            

