    private void categoryListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_categoryListValueChanged
        if(!evt.getValueIsAdjusting()){
            String s = categoryList.getSelectedValue();
            System.out.println(s);
            if(s.equals("Needed Items")){
                ArrayList<Item> items = inventory.getNeeded();
                ((DefaultListModel)categoryItems.getModel()).clear();
                for(int i = 0;i < items.size();i++){
                    ((DefaultListModel)categoryItems.getModel()).addElement(items.get(i).getItemName());
                }
            }
            else if(s.equals("Packages")){
                String packageInfo = new String();
                ArrayList<CarePackage> packageList = inventory.getPackageList();
                for(int i = 0; i < packageList.size();i++){
                    packageInfo += packageList.get(i);
                }
                packagesTextArea.setText("Package List: \n\n" + packageInfo);
                packagesPopUp.setVisible(true);
            }
            else{
                ArrayList<Item> items = inventory.getItemsOfType(s);
               ((DefaultListModel)categoryItems.getModel()).clear();
                for(int i = 0;i < items.size();i++){
                    ((DefaultListModel)categoryItems.getModel()).addElement(items.get(i).getItemName());
                }
            }
        }
        System.out.println(inventory);
    }//GEN-LAST:event_categoryListValueChanged

